export const getMatchesByStatus = (status: Match['status']): Match[] => {
  return mockMatches.filter((match) => match.status === status);
};

export interface LeagueGroup {
  league: string;
  matches: Match[];
}

export const getMatchesGroupedByLeague = (status: Match['status']): LeagueGroup[] => {
  const matches = getMatchesByStatus(status);
  const grouped = matches.reduce((acc, match) => {
    const existing = acc.find((group) => group.league === match.league);
    if (existing) {
      existing.matches.push(match);
    } else {
      acc.push({ league: match.league, matches: [match] });
    }
    return acc;
  }, [] as LeagueGroup[]);
  return grouped;
};
